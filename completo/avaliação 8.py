from random import randint

nota = []
a = 0
menor = 10
maior = 0
alpior = 0
almelhor = 0

for i in range(20):

    #num = randint(0,10)
    num = float(input("digite as notas dos alunos: "))
    nota.append(num)

for j in range(20):

    a = nota[j]

    if(a > maior):

        maior = a
    
    if(a < menor):

        menor = a

    

for k in range (20):

    a = nota[k]

    if(a == maior):
        almelhor += 1
    elif(a == menor):
        alpior += 1


print(f"maior nota é {maior}, menor nota é {menor}")
print(f"a quantidade de alunos que tiraram as melhores notas são {almelhor}, e as piores notas são {alpior}")
